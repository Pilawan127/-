print("โปรแกรมคำนวณพื้นที่รูปทรงต่าง ๆ")

print("1. สี่เหลี่ยมผืนผ้า")
print("2. สามเหลี่ยม")
print("3. วงกลม")
print("4. สี่เหลี่ยมจัตุรัส")

เลือก = int(input("เลือกประเภทที่ต้องการคำนวณ: "))

if เลือก == 1:
    กว้าง = float(input("กรอกความกว้าง: "))
    ยาว = float(input("กรอกความยาว: "))
    พื้นที่ = กว้าง * ยาว
    print("พื้นที่สี่เหลี่ยมผืนผ้า =", พื้นที่, "ตารางหน่วย")

elif เลือก == 2:
    ฐาน = float(input("กรอกความยาวฐาน: "))
    สูง = float(input("กรอกความสูง: "))
    พื้นที่ = 0.5 * ฐาน * สูง
    print("พื้นที่สามเหลี่ยม =", พื้นที่, "ตารางหน่วย")

elif เลือก == 3:
    รัศมี = float(input("กรอกรัศมีวงกลม: "))
    พื้นที่ = 3.14159 * รัศมี * รัศมี
    print("พื้นที่วงกลม =", พื้นที่, "ตารางหน่วย")

elif เลือก == 4:
    ด้าน = float(input("กรอกความยาวด้าน: "))
    พื้นที่ = ด้าน * ด้าน
    print("พื้นที่สี่เหลี่ยมจัตุรัส =", พื้นที่, "ตารางหน่วย")

else:
    print("กรุณาเลือกหมายเลข 1-4")